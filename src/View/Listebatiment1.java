    public void Load_Table() {
        BatimentImplementeDAO bl = new BatimentImplementeDAO();
        List<Batimentc> List = bl.list();
        DefaultTableModel df = (DefaultTableModel) ListeBatiment.getModel();
        df.setRowCount(0);
        
        List.forEach((b) -> {
            df.addRow(new Object[]{
                b.getId(),
                b.getDesignation(),
                b.getNombreCellule(),
                b.getDate_de_construction()
            });
        });
    }
    

